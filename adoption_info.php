<h3 > <b>Myth #1: I don’t know what I’m getting</b></h3>
<p >There may in fact be more information available about an adoptable pet than one from a breeder or pet store.
Many of the pets posted on Petfinder are in foster care. Foster parents live with their charges 24-7 and can often tell you, in detail, about the pet’s personality and habits. If the pet is at a shelter, the staff or volunteers may be able to tell you what he or she is like.
    At the very least, you can ask the staff if the pet was an owner surrender (rather than a stray) and, if so, what the former owner said about him or her. Quite often pets are given up because the owner faced financial or housing issues (more on that later). You can also ask about the health and behavioral evaluations the pet has undergone since arriving at the shelter. In contrast, pet store owners rarely have an idea of what a pet will be like in a home.</p>
    
    <h3><b>Myth #2: I can’t find what I want at a shelter</b></h3>
<p >If you can’t find the pet you’re looking for on Petfinder, don’t give up. Some shelters maintain waiting lists for specific breeds, so don’t be afraid to ask! There are also breed-specific rescues for just about every breed, and most of them post their pets on Petfinder. (Petfinder can even e-mail you when a pet that fits your criteria is posted — just click “Save this Search” at the top of your search results page.)</p>

    <h3><b>Myth #3: I can get a free pet, so why pay an adoption fee?</b></h3>
<p >According to the National Council on Pet Population Study and Policy approximately 65% of pet parents in the U.S. get their pets for free or at low cost, and most pets are obtained from acquaintances or family members. The NCPPSP also reports that pets acquired from friends make up more than 30% of pets surrendered to shelters 
While getting a “free” pet may seem like a bargain at first, you’re then responsible for veterinary costs that shelters and rescue groups usually cover, including:</p>
    <ul  class="container my-4 mt-3">
    
        <li>Spaying/neutering 1500-3000</li>
        <li>Distemper vaccination 2000-3000 x2</li>
        <li>Rabies vaccination 1500-2500</li>
        <li>Heartworm test 1500-3500</li>
        <li>Flea/tick treatment 5500-8000</li>
        <li>Microchip 3500 </li>
    
    </ul>

<h3><b>Myth #4: Pets are in shelters because they didn’t make good pets</b></h3>
<p >In fact, the main reasons pets are given up include:</p>
    <ul class="container my-4 mt-3 ">
        <li>Owners are moving to housing that don’t allow pets (7% dogs, 8% cats)</li>
        <li>Allergies (8% cats)</li>
        <li>Owner having personal problems (4% dogs and cats)</li>
        <li>Too many or no room for litter mates (7% dogs, 17% cats)</li>
        <li>Owner can no longer afford the pet (5% dogs, 6% cats)</li>
        <li>Owner no longer has time for the pet (4% dogs)</li>
        
    </ul>
<p>As you can see, many of the reasons have nothing to do with the pets themselves. Working with shelter staff and volunteers can be a great way to figure out the best match for you and your home.</p>

<h3><b>Myth #5: Shelter pets have too much baggage</b></h3>

<p>Rescued pets have full histories. something that can actually be GREAT for adopters. Remember, all pets– even eight-week old puppies and kittens — have distinct personalities. Those personalities will either jive with your home and lifestyle or not.  Work with rescue group or shelter staff to find the right fit for you.</p>
